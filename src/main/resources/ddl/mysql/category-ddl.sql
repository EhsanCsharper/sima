CREATE TABLE T_CATEGORYELEMENT 
   (	ID BIGINT(20) NOT NULL AUTO_INCREMENT,
	C_VERSION INT, 
	C_ACTIVE BIT(1), 
	C_VALUE VARCHAR(2024), 
	C_MANUALID VARCHAR(255), 
	C_ENABLE BIT(1), 
	C_CATEGORY BIGINT(20), 
	I_CATEGORY INT, 
	 PRIMARY KEY (ID)
);

create index CATEGORYELEMENT_CATEGORY on T_CATEGORYELEMENT (C_CATEGORY);

