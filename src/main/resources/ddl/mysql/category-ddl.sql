CREATE TABLE T_CATEGORY 
   (	ID BIGINT(20) NOT NULL AUTO_INCREMENT,
	C_VERSION INT, 
	C_ACTIVE BIT(1), 
	C_MANUALID VARCHAR(255), 
	C_NAME VARCHAR(255) unique, 
	C_ENABLE BIT(1), 
	 PRIMARY KEY (ID)
);

