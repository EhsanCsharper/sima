   (	ID BIGINT(20) NOT NULL AUTO_INCREMENT, 
	C_VERSION INT, 
	C_ACTIVE BIT(1), 
	C_BLOB BLOB, 
	C_CREATIONDATE VARCHAR(256), 
	C_MANUALID VARCHAR(255), 
	C_ENABLE BIT(1),
	primary key (ID)
   );
